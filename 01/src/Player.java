
public class Player {
	
	private int number;	

	private int wins;
	
	private int sumPairs;
	
	public int getNumber() {
		
		return number;
		
	}

	public void setNumber(int number) {
		
		this.number = number;
		
	}

	public int getWins() {
		
		return wins;
		
	}

	public void setWins(int wins) {
		
		this.wins = wins;
		
	}

	public int getSumPairs() {
		
		return sumPairs;
		
	}

	public void setSumPairs(int sumPairs) {
		
		this.sumPairs = sumPairs;
		
	}
}
